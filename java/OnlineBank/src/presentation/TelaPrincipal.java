    private void alteraContaAtual(JComboBox<String> comboBox) {
        
        // O setSelectedIndex e addItem das combobox despoletam "change", entrando aqui novamente.
        // por isso mudamos o valor de "isResetting" para true quando nao queremos que entre em ciclo
        if (isResetting) {
            return;
        }
        

        Conta.Tipo tipoSelecionado = null;

        for (Conta.Tipo t : Conta.Tipo.values()) {
            if (comboBox.getSelectedItem().equals(t.toString())) {
                tipoSelecionado = t;
            }
        }

        if (contaAtual == null || !contaAtual.getTipo().equals(tipoSelecionado)) {
            for (Conta conta : cliente.getContas()) {

                if (conta.getTipo().equals(tipoSelecionado)) {
                    contaAtual = conta;
                    break;
                }
            }
        }
        
        
        // vamos por a true para nao voltar a entrar aqui
        isResetting=true;
        cbxTipoConta.setSelectedIndex(comboBox.getSelectedIndex());
        cbxContaOrigem.setSelectedIndex(comboBox.getSelectedIndex());
        
        //vamos voltar a por a false para que volte tudo ao "normal"
        isResetting=false;
        
        labelNumeroContaHome.setText(contaAtual.getNumeroConta());
        labelSaldoHome.setText("€ " + contaAtual.getSaldo().toString());
    }
