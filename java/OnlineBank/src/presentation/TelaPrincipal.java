    /**
     * Atualiza movimentos
     */
    public void atualizaGrid() {

        String cols[] = {"Data", "Tipo", "Montante", "D/C", "Saldo"};
        DefaultTableModel dlm = new DefaultTableModel(cols, 0);

        try {

            ArrayList<Movimento> movimentos = db.getMovimentosCliente(cliente);

            for (Movimento m : movimentos) {
                String mv[] = {m.getDataHora().toString(), m.getConta().getTipo().toString(), m.getMontante().toString(), m.getTipo().toString(), m.getSaldo().toString()};
                dlm.addRow(mv);
            }

            tableMovimentosHome.setModel(dlm);
            tableTranfer.setModel(dlm);
        } catch (Exception e) {
            MessageBox.showError(e);
        }
    }

