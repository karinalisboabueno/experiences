    public void initCliente() {

       

        try {
            isResetting = true;
            
            cliente = db.loadCliente(cliente.getId());
            
            contaAtual = null;

            this.panelContasCliente1.setCliente(cliente);

            for (Conta c : cliente.getContas()) {

                if (c.getTipo().equals(Conta.Tipo.POUPANCA)) {
                    esconderComponente(panelCriarContaPoupanca, true);
                }

            }
            
            cbxTipoConta.removeAllItems();
            cbxContaOrigem.removeAllItems();
            
            for (Conta conta : cliente.getContas()) {
                cbxTipoConta.addItem(conta.getTipo().toString());
                cbxContaOrigem.addItem(conta.getTipo().toString());
            }

            labelNomeHome.setText("Olá " + cliente.getNome() + "!");
            txtNomePerfil.setText(cliente.getNome());
            txtEmailPerfil.setText(cliente.getEmail());
            txtEnderecoPerfil.setText(cliente.getEndereco());

            atualizaGrid();
            isResetting = false;
            
            cbxTipoConta.setSelectedIndex(0);  
            cbxContaOrigem.setSelectedIndex(0);
            
            cbxTipoContaItemStateChanged(null);
            

        } catch (Exception e) {
            MessageBox.showError(e);
        }

    }

