    private void btnCriarContaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnCriarContaMouseClicked

        if (txtCriarSenha.getText().equals(txtRepetirSenha.getText()) && !txtMontante.getText().isEmpty()) {

            try {
                Cliente cliente = new Cliente();
                cliente.setNome(txtNome.getText());
                cliente.setEmail(txtEmail.getText());
                cliente.setEndereco(txtEndereco.getText());
                cliente.setSenha(txtCriarSenha.getText());
                
                BigDecimal montante = BigDecimal.ZERO;
                
                try{
                   montante = new BigDecimal(txtMontante.getText());
                }catch(NumberFormatException e){
                    throw new Exception("Montante com valor inv√°lido", e);
                }
                
                if(montante.longValue()<500){
                    throw new Exception("O montante tem de ser igual ou superior a 500!");
                }

                dbManager.criarCliente(cliente, montante);

                CriadoSucesso cs = new CriadoSucesso(cliente);

                setVisible(false);
                cs.setVisible(true);
                dispose();

            } catch (Exception e) {
                MessageBox.showError(e);
                return;
            }

        } else {

            MessageBox.showError("As senhas n√£o coincidem!");

        }

    }//GEN-LAST:event_btnCriarContaMouseClicked

